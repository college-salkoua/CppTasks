#include <iostream>

int main() {
    double numbers[5]; // Масив для зберігання п'яти чисел
    double max; // Змінна для зберігання найбільшого числа

    // Введення п'яти дійсних чисел
    std::cout << "Введіть п'ять дійсних чисел:\n";
    for (int i = 0; i < 5; ++i) {
        std::cin >> numbers[i];
    }

    // Спочатку припускаємо, що перше число є найбільшим
    max = numbers[0];

    // Порівнюємо з рештою чисел
    for (int i = 1; i < 5; ++i) {
        if (numbers[i] > max) {
            max = numbers[i]; // Зберігаємо нове найбільше число
        }
    }

    // Виводимо найбільше число
    std::cout << "Найбільше число: " << max << std::endl;

    return 0;
}

